# String

# Slicing
name = "shubham sutar"

print(name[1:6])
print(name[-6:-1])
print(name[:8])
print(name[-1:])
print(name[:-1])
print(name[2:-1])
print(name[:])

# Slicing with skip value

print(name[0:11:2])  # shubham sut - suhmst

# Functions of String
name1 = "I love my India"
print(len(name1))  # length of string
print(name1.endswith("dia"))  # True
print(name1.startswith("I"))  # True
print(name, end=" sir")  # shubham sutar sir
print(name1, name, sep=",")
print(name.capitalize())
print(name.title())












