# problem 03
# 3. How do you prevent a python print() function to print a new line at the end
# Ans --> By using end=""


print("Hello shubham")  # print at new line
print("Hello sayali")  # print at new line

# we want to prevent new line

print("Hello shubham", end=" ")  # print at new line
print("Hello sayali")  # print at new line

# output = Hello shubham Hello sayali
