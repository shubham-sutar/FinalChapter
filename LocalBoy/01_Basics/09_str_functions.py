# string functions

name = "hello world"

print(len(name))
print(name.endswith("rld"))  # True
print(name.endswith("ald"))  # False

print(name.startswith("ald"))  # False
print(name.startswith("hel"))  # True

print(name.capitalize())  # Hello world
print(name.title())  # Hello World

print(name.replace("world", "sayali"))  # hello sayali




