# string functions

name = "shubham"
print(type(name))  # type ---> shows data type
print(id(name))  # id ---> showing memory address
print(len(name))  # len ---> length, starts from 1

chara = name.count('h')  # ---> Count the characters
print(chara)

print(name[2])  # ---> second index character of string i.e "u"
print(name[-1])  # ---> reverse index "m"
